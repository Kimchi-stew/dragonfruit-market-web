import type { Product } from '../components/product/ProductCard'

let _id = 1
const p = (
  name: string,
  brand: string,
  price: number,
  rating: number,
  reviewCount: number,
  category: string,
  subcategory: string,
  attrs: Record<string, string | string[]>,
  discountRate?: number,
  imgSeed?: string,
): Product => {
  const id = _id++
  const originalPrice = discountRate ? Math.round(price / (1 - discountRate / 100) / 100) * 100 : undefined
  const seed = imgSeed ?? `product${id}`
  return {
    id,
    name,
    brand,
    price,
    originalPrice,
    discountRate,
    rating,
    reviewCount,
    image: `https://picsum.photos/seed/${seed}/400/400`,
    category,
    subcategory,
    attrs,
  }
}

